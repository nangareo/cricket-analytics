def get_initials(name):
    parts = name.strip().split()
    if len(parts) >= 2:
        return (parts[0][0] + parts[-1][0]).upper()
    return name[:2].upper()

def generate_avatar_svg(player_name, size=48):
    import hashlib
    initials = get_initials(player_name)
    color1   = get_team_color(player_name)
    color2   = get_team_secondary(player_name)
    if color2 in ["#000000","#000","#1e2d3d","#444","#C41010","#B22222"]:
        h  = int(hashlib.md5(player_name.encode()).hexdigest()[:6], 16)
        r  = min((h >> 16 & 0xFF) + 80, 255)
        g  = min((h >> 8  & 0xFF) + 80, 255)
        b  = min((h       & 0xFF) + 80, 255)
        color2 = f"#{r:02x}{g:02x}{b:02x}"
    uid = hashlib.md5(f"{player_name}{size}".encode()).hexdigest()[:8]
    svg = (f'<svg xmlns="http://www.w3.org/2000/svg" width="{size}" height="{size}" viewBox="0 0 48 48">'
           f'<defs><linearGradient id="g{uid}" x1="0%" y1="0%" x2="100%" y2="100%">'
           f'<stop offset="0%" stop-color="{color1}"/>'
           f'<stop offset="100%" stop-color="{color2}"/>'
           f'</linearGradient></defs>'
           f'<circle cx="24" cy="24" r="23" fill="url(#g{uid})"/>'
           f'<circle cx="24" cy="24" r="23" fill="none" stroke="rgba(255,255,255,0.25)" stroke-width="1.5"/>'
           f'<text x="24" y="30" font-family="Arial,sans-serif" font-size="16" '
           f'font-weight="800" fill="white" text-anchor="middle">{initials}</text>'
           f'</svg>')
    return svg

def avatar_html(player_name, size=36):
    import base64
    svg   = generate_avatar_svg(player_name, size)
    b64   = base64.b64encode(svg.encode()).decode()
    color = get_team_color(player_name)
    return (f'<img src="data:image/svg+xml;base64,{b64}" '
            f'width="{size}" height="{size}" '
            f'style="border-radius:50%;border:2px solid {color};'
            f'vertical-align:middle;margin-right:6px;flex-shrink:0" />')
